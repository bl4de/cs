import string


def print_rangoli(size):
    alphabet = string.ascii_lowercase
    rows = []
    # create middle row
    row = ''
    for iter in range(size - 1, 0, -1):
        row += alphabet[iter] + '-'
    for iter in range(0, size):
        row += alphabet[iter] + ('-' if iter < size - 1 else '')

    rows.append(row)

    print("\n".join(rows))
